  <h1>GLOBAL VILLAGE VIDIOT</h1>
  <h3>ATOMIC ELROY</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-11">Friday Flash #11</a> exhibition on Friday September 25th, 2015. </p>
  <p> Artist's Statement:<br/>
I make my work by using images as materials. Sometimes they move. I capture electronic images and manipulate them into art. Unlike some artists, I tend to use images that I have created. I combine these images with actions, sounds, and locations to make performance and installations with the intention of making you feel something, unsettledness, mirth, discomfort, happiness, disgust, etc. I break conventions as to break down expectations between viewer and artist, and expose the peculiarities of humanity.</p>
<p>

