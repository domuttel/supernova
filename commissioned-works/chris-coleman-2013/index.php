<h1>METRO RE/DE-CONSTRUCTION</h1>
<h3>CHRIS COLEMAN</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-5">Friday Flash #5</a> exhibition on Friday September 20th, 2013. </p>
<p>Artist's Statement:<br/>
METRO is a journey, beginning outside and moving inwards. Sometimes enveloping and sometimes sparse and shattered, time produces forward motion and simplification. It is documentation of a physical space, captured millimeter accurate coordinates, colors and varied textures. Several real journeys have been digitally distilled to become a beautifully abstracted experience, not unlike to the disconnection one has while riding public transport.</p>
<p>With a handheld 3D scanning device and a laptop, the artist rode the Denver Light Rail as he does every day for his commute. The train was scanned by walking through the car from one end to the other while en-route and train stations were captured with short strolls between stops. The fragmentation and gaps in data are defined by the physical bumps, speed, and curves in the journeys affecting the hand of the artist while scanning. While the final models are still, they are in fact documents of time, perspective and perception. In this way no two scans will ever be the same; each is documentation of that unique body on that particular journey.</p>
