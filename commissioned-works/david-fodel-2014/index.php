<h1>WELD</h1>
<h3>DAVID FODEL</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-8">Friday Flash #8</a> exhibition on Friday September 19th, 2014. </p>

<p>Artist's Statement:<br/>Weld is constructed using high-speed industrial machine vision technology from IO Industries. The material is sourced from approx. 1100 fps video capture of a MIG Welder and then processed using software that generates a 3D mesh and height map based on the luminance of the image. The result is pretty interesting. I liked how it began to resemble a naturalistic kind of mythic landscape, volcanic, primordial, epic. I crafted a soundscape to match this feeling, and in the end layered on top what I thought of as a kind of Frippertronics inspired over the top epicosity thing.</p>
