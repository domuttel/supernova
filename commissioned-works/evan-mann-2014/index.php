<h1>PURE CONCENTRICITY</h1>
<h3>EVAN MANN</h3>
 <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-8">Friday Flash #8</a> exhibition on Friday September 19th, 2014. </p>

<p>Artist's Statement:<br/>The film is composed of sequential photographs while being visually limited to the natural landscape, the human figure, and concentric circles of shaving cream.</p>

<p>I must acknowledge my appreciation for the following people who contributed their time and energy into the making of this film: Caleb Young, Matthew Jorgenson, Brandon McCalley, Chau and Kevin Gan, and my wife, Deborah. Figural abstractions are a reference to the photographic work of Bill Durgin.</p>
