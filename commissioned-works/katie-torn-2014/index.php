  <h1>BREATH DEEP</h1>
  <h3>KATIE TORN</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-8">Friday Flash #8</a> exhibition on Friday September 19th, 2014. </p>

<p>Artist's Statement:<br/>Breathe Deep is a film about a virtual sculpture that could only exist in the realm of the digital combining natural and synthetic, human and avatar, plant and machine. A dystopic female figure has become one with a pile of debris. She is a virtual architectur- al ecosystem made of gifs, 3d models, plastic and fake liquids. This is represented through a combination of live action video of performance with sculptures composited with 3D animation and special effects.</p>

