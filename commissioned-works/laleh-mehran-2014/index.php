  <h1>DOMINANT POLICY</h1>
  <h3>LALEH MEHRAN</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-8">Friday Flash #8</a> exhibition on Friday September 19th, 2014. </p>

<p>Artist's Statement:<br/>Dominant Policy transforms various international currencies into a mesmerizing kaleidoscope, evolving from abstraction to revealing of recognizable elements. The video encompasses decades of graphical change as monarchies reign, regimes fall, values fluctuate, and counterfeiting increases. Since their invention, currencies have been based on complex systems dictating value and ultimately determining sociopolitical power. In Dominant Policy, Mehran treats all currency with the same presence, from the smallest to the unattainably large denominations, collapsing their inherent worth. Layers of intricately patterned colors and lines, specialty papers woven with security threads and holograms attempt to retain control over the definitions of physical currency, all while the majority of monetary value moves at light speed in the form of zeros and ones.</p>

