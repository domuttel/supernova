  <h1>DIAGNOSTIC TOOLS</h1>
  <h3>MIKE PELLETIER</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-11">Friday Flash #11</a> exhibition on Friday September 25th, 2015. </p>
  <p> Artist's Statement:<br/>
The animation "Diagnostic Tool" is set in an environment with medical equipment suggesting some sort of research area. Surrounded by tools used to understand the body and to understand the world, figures driven by motion capture data  on the verge of malfunctioning combined with 3d scanned sculptures cycle through a series of repeated and offset actions. In my work I attempt to examine how digital technologies are used to both capture information about ourselves and create images of ourselves and how that relates to art practice throughout history. I’m interested in the points where this information gets corrupted or distorted and how it causes us to see ourselves in different ways. In my work characteristics relating to a person’s movement are transferrable and mapped to new bodies and the surface of the body is just a description and that description can be changed and modified.</p>

