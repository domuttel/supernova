  <h1>INTERIOR MEGAPOLIS TEMPLE</h1>
  <h3>MILTON CROISSANT III</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/sightline">Urban Encounters: Sightline</a> exhibition on Friday, May 11th, 2012. </p>

<p>Artist's Statement:<br/>Made with opensource software, this animation considers the vast cellular space within major metropolitan centers. These interior spaces are shrines to the isolated room of the gamer/Otaku, they are of the hikikomori's dreamtime. Created as a commisioned work for the show Urban Encounters: an outdoor video show of national and international artists screened on the "Jumbo Tron" outside of the Denver Performing Arts Complex.</p>
