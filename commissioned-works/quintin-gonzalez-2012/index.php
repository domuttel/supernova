  <h1>LA TIERRA</h1>
  <h3>QUINTIN GONZALEZ</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/sightline">Urban Encounters: Sightline</a> exhibition on Friday, May 11th, 2012. </p>

<p>Artist's Statement:<br/>La Tierra is a 2012 short experimental video/animation that explores the image of the Mexican/Chicano masked wrestler (Spanish: luchador) which serves as a visual symbol of the struggle to overcome the colonized mind. The main figure portrays an embodiment of the two-sided face of post-colonial trauma, a descendant of both the colonized and colonizer.</p>

<p>La Tierra seeks to convey a sense of empowerment by expressing the interplay of both fantasy and truth. Where an empathy for the character evokes a type of social mirror and the masked wrestler is both a true hero existing only in longing and dreams and is the duality between wish and veracity while conveying experiences that are heroic, romantic and at times tragic. The luchador exists in a state of being where the voices of civil rights and the social justice struggles of the past echo as a resounding roar that may give hope to the labors and hardships of the immigrants of today.</p>

<p>The colonized and descendants of the colonized have been put through a trial that has never been based in reason. If we look at history, the adversaries of equality have acted from cruel emotions and biases. In a sense it is my hope to call upon the act of compassion and for others to understand the courage and solemn dignity of the immigrant in America. La Tierra shows torment but moves towards a sense of tranquil release in the liberation that comes at the end where the figure of death emerges and faces the tormented figure and who does not look away. She is the death of his anguish ultimately creating a new reality of hope and triumph.</p>
