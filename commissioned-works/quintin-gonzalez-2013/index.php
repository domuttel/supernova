  <h1>THE ROAR OF PARADISE</h1>
  <h3>QUINTIN GONZALEZ</h3>
   <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-5">Friday Flash #5</a> exhibition on Friday September 20th, 2013. </p>


<p>Artist's Statement:<br/>In the video/animation “The Roar of Paradise”, the theme of the balance of opposites is depicted as existing within a cyber space that looks toward the infinite as a metaphor for the metamorphosis of identity. The central narrative is the mutable character of individual identity as existing simultaneously in both a celestial and physical existence. In this piece existence is infinite and ever changing as the images and patters express the ongoing balance of the opposites of life and death. And how the self perpetually evolves, assuming different guises to find a oneness to the human condition that ultimately has no destination and exists in a void alone to perceive, to see and know the cycles of the countless facades of profound change, where the journey itself is its own destination.</p>

