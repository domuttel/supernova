  <h1>1000 YEARS</h1>
  <h3>SIN YOUNG KIM / PNSY STUDIO</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-11">Friday Flash #11</a> exhibition on Friday September 25th, 2015. </p>
  <p> Artist's Statement:<br/>
As a person who lives alone, I often find myself drifting in my own thoughts - this piece is a visualization, a metaphorical portrait of what I think / say in my mind, in those moments. Most of the times, these little bits of narrations are nothing but short fragments of my thoughts, but when they're put together, it almost sounds like poetic lines from a movie. By using various 3d models rendered differently in each sequence, and by putting subtitles to the audio,
rather than having the audience understanding or observing my thoughts, I want them to feel swept away by the abundant and scattered flow of my thoughts.
  </p>

