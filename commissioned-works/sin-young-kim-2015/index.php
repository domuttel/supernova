  <h1>25 YEARS</h1>
  <h3>SIN YOUNG KIM / PNSY STUDIO</h3>
  <p>This piece was commissioned by Denver Digerati and unveiled during the <a href="../../video-archive/friday-flash-11">Friday Flash #11</a> exhibition on Friday September 25th, 2015. </p>
  <p> Artist's Statement:<br/>
 For this piece I took the focus on more personal perspective - I decided to took my age, 25 - and starting to think about it. 25 years old is such a weird age that divides the 20s, not young or not quite old, feels very confused to me. To visually interpret this I set the time to my birthday January 23rd, 1990 till today 2015, creating the 25 years flashing away in 2 minutes. The background music is composed of songs that I used to listened to from my childhood till now, creating an array of almost disturbing mixture of sound - which represents such chaotic phase that I'm at now.
  </p>

