<h1>FRIDAY FLASH #2: CROWD > CONTROL</h1>
<h3>2013</h3>
<p>This Promo was created for the performance by David Fodel and Paco Proano on June 14th at the corner of 14th and champa in Denver.</p>
<p>crowd>control playfully engages public media space by turning the corner of 14th and champa into a live audio-visual instrument. using the big-screen LED panel and software for tracking people in public spaces the artists construct a system that is interactively changed by the movement of the people and vehicles. Sound and image become entangled with bodies and technology in an artwork that is part game, part instrument.</p>
<p>crowd>control is sponsored in part by Artemis Vision, a Denver-based machine vision systems integrator. More info at <a href="http://www.artemvision.com" target="_blank">artemisvision.com</a>.</p>
