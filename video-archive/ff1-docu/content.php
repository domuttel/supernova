<h1>FRIDAY FLASH #1 DOCUMENT</h1>
<h3>2013</h3>
<p>Denver Digerati launched the first Friday Flash program on May 24th, 2013 on the Denver Theatre District's premier LED screen at 14th and Champa Street. The program was the first in a five-month series that brought the best in motion-based art and animation from around the world to the public in an exciting context at one of the most prominent corners in the heart of downtown Denver. 'Friday Flash #1' was curated by Denver Digerati associates Ivar Zeile and Ryan Pattie, along with contributions from <a href="http://artsandvenuesdenver.com/" target="_blank">Denver Arts & Venues'</a> Tariana Navas-Nieves.</p>

<p>This documentation of the evening was shot and edited by Denver Digerati associate curator Ryan Pattie, delivering a brief look at the artwork on display as well as the audience reaction from the event.</p>

