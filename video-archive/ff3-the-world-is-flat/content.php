<h1>FRIDAY FLASH #3: THE WORLD IS !FLAT</h1>
<h3>2013</h3>
<p> "The World is !Flat" presents short-form animations and motion-based artworks by a select group of female artist who are building a strong presence in the international sphere of digital art. Denver artists Chris Coleman was asked to curate the program for Denver Digerati, based on his previous involvement with Digerati related programs, his stature in the local community as well as his growing international presence within the growing new-media sphere. Coleman is one of seven artists commissioned by Denver Digerati to create new works for the Denver Theatre District's permanent collection in 2013. Coleman is an Associate Professor of Emergent Digital Practices at the University of Denver, specializing in video, installation, gizmology, animation, and sculpture.</p>
<p>Coleman selections for Friday Flash #3 reflect his personal interest in how digital media can alter space and reflect new concepts of landscape. The title for his program reflects this alteration of reality with the landscape as well as the ever shifting symbolic language that is inherent in our technology age, the exclamation point preceding Flat known in digital circles to mean "not." Coleman sought to find emerging artists that he was not already familiar with for the project, rather than rely on associations he's already built through his professional and teaching network at DU.</p>

<p>The program features works by<br/>
Sama Alshaibi<br/>
Brianna Lowe<br/>
Sara Ludy<br/>
Rosa Menkman<br/>
Brenna Murphy<br/>
Katie Torn<br/>
Sabine Gruffat<br/>
and Angela Washko.</p>
