<h1>FRIDAY FLASH #4: REDLINE ARTISTS</h1>
<h3>2013</h3>
<p>The Denver Theatre District and Denver Digerati presented Friday Flash #4: RedLine Resident Artists on Friday August 9th, 2013.</p>
<p><a href="http://redlineart.org/" target="_blank">RedLine</a> is a diverse urban laboratory where art, education and community converge, and one of the most respected artist residency programs in Colorado. RedLine engages approximately 15 to 20 artists at any given time for durations of up to three years, with a structure that supports both emerging and mentoring contemporary artists. Friday Flash #4  presented works by some of the current crop of resident artists who have a concentration in motion-based art or have projects that extend into that realm.</p>

<p>Featured Artists:<br/>
Katie Martineau-Caron<br/>
Nikki Pike<br/>
Katie Watson<br/>
Lurke (Laura Shill in collaboration with Burke Miles)<br/>
Donald Fodness (in collaboration with Barry Whittaker and Mike Bernhardt)<br/>
Bryan Leister<br/>
Justin Beard</p>
