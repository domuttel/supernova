<h1>FRAME OF MIND</h1>
<h3>2011</h3>
<p>The signature program for 2011 was the half-hour exhibition 'Frame of Mind,'  a central component to that year's Create Denver Week celebration 'Local Network', part of an evening event supporting creative endeavors from within the Denver community. Frame of Mind was culled from a locals-only call for entries of motion based artworks of one minute or less. &nbsp;39 of the entries were selected and showcased to the public during the event. Frame of Mind also awarded a best-of cash prize to artist David Fodel for his 52 second work 'Nimitta'. Select works from the program were later put into a regular rotation on the LED screen alongside the regular commercial content.</p>
