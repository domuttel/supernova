<h1>FRIDAY FLASH #1</h1>
<h3>2012</h3>
<p>Denver Digerati and the Denver Theatre District presented their first Friday Flash program on May 24th to an enthusiastic public in downtown Denver, Colorado. Friday Flash is a monthly series of public screenings that take place on the 14th and Champa LED screen in the heart of the city. The program delivers a diverse mixture of outstanding works being created across the world from within the fields of animation, motion based art and interactive new media. Friday Flash #1 was curated by Denver Digerati's creative director Ivar Zeile and assistant Ryan Pattie, along with selections from Denver Arts &amp; Venue's newest curator Tariana Navas-Nieves.</p>
<p>The show features the following artists:<br/>
Faiyaz Jafri<br/>
Protey Temen<br/>
Stephanie Davidson<br/>
Jason Akira Somma<br/>
Jake Fried<br/>
Sumit Sijher<br/>
Diego Lama<br/>
Atomic Elroy</p>
