  <h1>FRIDAY FLASH #11: COMMISSIONED WORKS</h1>
  <h3>2015</h3>


<p>Denver Digerati concluded the 2015 summer Friday Flash series with the pinnacle of our curatorial and support efforts, unveiling seven new artworks commissioned specifically for our premier LED screen and growing library of motion-based content for public display. Our third set of commissioned artists once again bridges between Denver's thriving local art scene and the greater global network that has embraced our unique protocol through previous programs.</p>

<p>Denver Digerati presents new works by Faiyaz Jafri (Hong Kong) and Mike Pelletier (The Netherlands), both of who have been featured previously in our separate Friday Flash curatorial programs as well as this year's '10-second film festival.' Also returning to our screen is Brenna Murphy (Portland, OR), Sin Young Kim (New York, NY) and Atomic Elroy (Sacramento, CA), all of whom have been previously featured in Friday Flash curatorial programs. And Denver is represented this year through new works by Mario Zoots as well as Katie Watson and Tara Pattie of Impromptu Company.</p>

