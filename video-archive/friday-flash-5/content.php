<h1>FRIDAY FLASH #5: COMMISSIONED WORKS</h1>
<h3>2013</h3>
<p>Friday Flash #5 was the conclusion of Denver Digerati's summer 2013 series, featuring the unveiling of new, never before seen works by seven artists commissioned for the LED screen at 14th and Champa in Downtown Denver. Through the process of commissioning artists for works specific to screens in the Denver Theatre District, Denver Digerati emphasizes the potential of these screens as vehicles for presenting dynamic art content, showcasing leading advances in motion-based art and animation by regional and international artists for the public.</p>

<p>Featured Artists:<br/>
Chris Coleman (Denver, CO)<br/>
Bryan Leister (Denver, CO)<br/>
Quintin Gonzalez (Denver, CO)<br/>
Justin Beard (Denver, CO)<br/>
Alex McLeod (Toronto, Canada)<br/>
Jonathan Monaghan (Washington, DC)<br/>
Milton Croissant III (Baltimore, MD)</p>

