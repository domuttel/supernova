<h1>FRIDAY FLASH #8: COMMISSIONED WORKS</h1>
<h3>2014</h3>

<p> On Friday September 19th, 2014, the Denver Theatre District and Denver Digerati presented Friday Flash #8. This was the final program and capstone for the 2014 summer series. Friday Flash #8 introduced the newest additions to Denver Digerati's commissioned artwork collection. The artists created works specifically for Denver Theatre District's LED screens in Downtown Denver, and are now part of an ongoing collection, the first of it's kind in the world.</p>

<p>Of the 2014 commissions, four were created from Denver-based artists Laleh Mehran, Evan Mann, David Fodel and Jeff Jurich. Two additional commissions were created by New York artists Katie Torn and Jeremy Couillard, and a third by Barry Whittaker, now based in Toledo, Ohio but formerly a grad student at CU Boulder. </p> 

<p>The lineup of commissioned artists and the new works they created for the Denver Digerati program are as follows:<br/>
Jeremy Couillard - Cyclops Lifecycle<br/>
Jeff Jurich - River In a Cup<br/>
Barry Whittaker - Throwing Things at the Sky to See if They Stick	<br/>
Laleh Mehran - Dominant Policy<br/>
David Fodel - Weld<br/>
Katie Torn - Breathe Deep<br/>
Evan Mann - Pure Concentricity<br/></p>
