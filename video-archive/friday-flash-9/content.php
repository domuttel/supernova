<h1>FRIDAY FLASH #9: MTVTM</h1>
<h3>2015</h3>

<p>Denver Digerati launched our 2015 Friday Flash series on Friday June 19th with FF No.9 - MTVTM or "Music to Video to Music," guest curated by Milton Melvin Croissant III. The program was shown on our signature LED Screen located at the corner of 14th and Champa in downtown Denver, Colorado.</p>

<p>Croissant crafted a very intriguing and broad-based program called MTVTM, or Music to Video to Music for Friday Flash No.9. The program explores today’s evolving world of music videos where the sound/music element is inseparable and features some of the most exciting up-and-coming visual artists and musicians of today. The selected works that will screen are also largely a showcase for evolving trends in digital art and animation, covering a wide range of styles that embody the visual play that is concurrent with advanced technological tools. Croissant compiled an assortment of engaging videos that include local, national and international artists working with musicians such as Holly Herndon, Dan Deacon, Lightning Bolt and others, and the showcase was even the platform launch for select works. The full lineup for the program consists of: </p> 

