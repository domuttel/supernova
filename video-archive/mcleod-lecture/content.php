<h1>ALEX MCLEOD LECTURE AT DU</h1>
<h3>2013</h3>
<p>Alex McLeod made a rare series of appearances in Denver, Colorado in September 2013. The University of Denver's Emergent Digital Practices program brought McLeod out as a visiting artist and guest lecturer the same week as Denver Digerati's Friday Flash program, which involved the unveiling of seven commissioned artworks including McLeod's <a href="../../commissioned-works/alex-mcleod-2013">Outworld Prism</a>. McLeod led a terrific discussion of his work and career, which was documented by the faculty and students of DU and cut together for this presentation.</p>

