<h1>RICK SILVA ON LED SCREEN</h1>
<p>In July 2012 Denver Digerati began outlining an intention to bring international artists to the public through the Large LED screens throughout the Denver Theatre District on a more regular basis. The first instance of this showcase was the premier of Rick Silva's Phil Morton Colorful Colorado (Rick Silva's 2011 Remix) on the Large LED screen outside the Performing Arts Center at 14th nd Champa in downtown Denver. This program went on to become Friday Flash, the monthly showcase of motion-based artwork accuring through the summer months in downtown Denver.</p>
<p>Rick Silva was one of the featured artists in the Boulder Museum of Contemporary Art's exhibition "Visual Rhythm," on view through September 9th, 2012.</p>
