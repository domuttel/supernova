<h1>SIGHTLINE SPECTATOR DOCUMENT</h1>
<h3>2012</h3>
<p>On the evening of May 11th, 2012 the Denver Theatre District in conjunction with Create Denver Week hosted 'Urban Encounters,' a program that brought several forward thinking creative concepts and spectacles to the streets of Denver, including SightLine, an hour and twenty minute program showcasing motion-based artwork created by local, national and international artists projected on downtown Denver's largest LED screen. The program started with a reception at the Spire across the street from the LED screen.</p>
<p>This document was compiled from footage shot that evening by spectators of the program, delivering a brief view of the events taking place around the program.</p>
<p> View the entire program <a href="../sightline">here</a>.</p>
<p>Thanks to all of the spectators and participants that submitted footage for this document and who supported this project. Special thanks to the DTD and Create Denver Week for their support of our curatorial effort.</p>
