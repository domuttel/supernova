<h1>URBAN ENCOUNTERS: SIGHTLINE</h1>
<h3>2012</h3>
<p>Urban Encounters: SightLine was the second curatorial effort led by the Denver Theatre District (DTD). SightLine took place on Friday, May 11th at 8pm, as one of the primary programs occurring that evening during Create Denver Week's 'Urban Encounters,' a showcase of the leading creativity and art coming from the Denver community.</p>

<p>Several works were commissioned by Denver Digerati for this program. Four of the commissioned works are from Denver-based artists Laleh Mehran, Evan Mann, David Fodel and Jeff Jurich. Two additional commissions were created by New York artists Katie Torn and Jeremy Couillard, and a third by Barry Whittaker, now based in Toledo, Ohio but formerly a grad student at CU Boulder.</p>

